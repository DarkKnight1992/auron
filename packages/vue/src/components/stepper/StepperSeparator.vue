<script setup lang="ts">
import { computed, inject } from 'vue'
import { stepperVariants } from '@auron/styles'
import { composeClassName } from '../../utils/composeClassName'
import { stepperContextKey } from './Stepper.context'

const props = withDefaults(defineProps<{
  class?: string
}>(), {
  class: undefined,
})

const ctx = inject(stepperContextKey)
const slotFns = computed(() =>
  stepperVariants({ orientation: ctx?.orientation.value }),
)
</script>

<template>
  <div
    :class="composeClassName(slotFns.separator(), props.class)"
    role="none"
    data-slot="stepper-separator"
  />
</template>
